static AVFrame *get_video_frame(OutputStream *ost, const uint8_t *pixels)
{
    AVCodecContext *c = ost->enc;

    /* check if we want to generate more frames */
    /*if (av_compare_ts(ost->next_pts, c->time_base,
                      STREAM_DURATION, (AVRational){ 1, 1 }) >= 0)
        return NULL;*/

    /* when we pass a frame to the encoder, it may keep a reference to it
     * internally; make sure we do not overwrite it here */
    if (av_frame_make_writable(ost->frame) < 0)
        exit(1);

    if (c->pix_fmt != AV_PIX_FMT_RGB32) {
        /* as we only generate a YUV420P picture, we must convert it
         * to the codec pixel format if needed */
        if (!ost->sws_ctx) {
            ost->sws_ctx = sws_getContext(c->width, c->height,
                                          AV_PIX_FMT_RGB32,
                                          c->width, c->height,
                                          c->pix_fmt,
                                          SCALE_FLAGS, NULL, NULL, NULL);
            if (!ost->sws_ctx) {
                fprintf(stderr,
                        "Could not initialize the conversion context\n");
                exit(1);
            }
        }
        //fill_yuv_image(ost->tmp_frame, ost->next_pts, c->width, c->height);
        
        for (int y = 0; y < c->height; y++)
            memcpy(&ost->tmp_frame->data[0][y*ost->tmp_frame->linesize[0]],
                &pixels[y*c->width*4], ost->tmp_frame->linesize[0]);

        sws_scale(ost->sws_ctx, (const uint8_t * const *) ost->tmp_frame->data,
                  ost->tmp_frame->linesize, 0, c->height, ost->frame->data,
                  ost->frame->linesize);
    } else {
        //fill_yuv_image(ost->frame, ost->next_pts, c->width, c->height);
        //memcpy(ost->frame->data[0], pixels, 4*c->width*c->height);
    }

    ost->frame->pts = ost->next_pts++;

    return ost->frame;
}

/*
 * encode one video frame and send it to the muxer
 * return 1 when encoding is finished, 0 otherwise
 */
static int write_video_frame(AVFormatContext *oc, OutputStream *ost, const uint8_t *pixels)
{
    int ret;
    AVCodecContext *c;
    AVFrame *frame;
    int got_packet = 0;
    AVPacket pkt = { 0 };

    c = ost->enc;

    frame = get_video_frame(ost, pixels);

    av_init_packet(&pkt);

    /* encode the image */
    ret = avcodec_encode_video2(c, &pkt, frame, &got_packet);
    if (ret < 0) {
        fprintf(stderr, "Error encoding video frame: %s\n", av_err2str(ret));
        exit(1);
    }

    if (got_packet) {
        ret = write_frame(oc, &c->time_base, ost->st, &pkt);
    } else {
        ret = 0;
    }

    if (ret < 0) {
        fprintf(stderr, "Error while writing video frame: %s\n", av_err2str(ret));
        exit(1);
    }

    return (frame || got_packet) ? 0 : 1;
}

static void close_stream(AVFormatContext *oc, OutputStream *ost)
{
    avcodec_free_context(&ost->enc);
    av_frame_free(&ost->frame);
    av_frame_free(&ost->tmp_frame);
    sws_freeContext(ost->sws_ctx);
    swr_free(&ost->swr_ctx);
}

struct VideoStream_impl {
    OutputStream video_st, audio_st;
    const char *filename;
    AVOutputFormat *fmt;
    AVFormatContext *oc;
    AVCodec *audio_codec, *video_codec;
    AVDictionary *opt;
};


void VideoStream::writeFrame(const std::vector<uint8_t>& pixels)
{
    write_video_frame(impl->oc, &impl->video_st, pixels.data());
}

VideoStream::~VideoStream()
{
    /* Write the trailer, if any. The trailer must be written before you
     * close the CodecContexts open when you wrote the header; otherwise
     * av_write_trailer() may try to use memory that was freed on
     * av_codec_close(). */
    av_write_trailer(impl->oc);

    /* Close each codec. */
    close_stream(impl->oc, &impl->video_st);

    if (!(impl->fmt->flags & AVFMT_NOFILE))
        /* Close the output file. */
        avio_closep(&impl->oc->pb);

    /* free the stream */
    avformat_free_context(impl->oc);
}

VideoStream::VideoStream(const char* filename)
: impl(new VideoStream_impl())
{
    int ret;
    int have_video, have_audio;
    int encode_video, encode_audio;

    memset(impl, 0, sizeof(VideoStream_impl));

    /* allocate the output media context */
    avformat_alloc_output_context2(&impl->oc, NULL, NULL, filename);
    if (!impl->oc) {
        printf("Could not deduce output format from file extension: using MPEG.\n");
        avformat_alloc_output_context2(&impl->oc, NULL, "mpeg", filename);
    }
    if (!impl->oc)
        throw -1;

    impl->fmt = impl->oc->oformat;

    /* Add the audio and video streams using the default format codecs
     * and initialize the codecs. */
    add_stream(&impl->video_st, impl->oc, &impl->video_codec, impl->fmt->video_codec);

    /* Now that all the parameters are set, we can open the audio and
     * video codecs and allocate the necessary encode buffers. */
    open_video(impl->oc, impl->video_codec, &impl->video_st, impl->opt);

    av_dump_format(impl->oc, 0, filename, 1);

    /* open the output file, if needed */
    if (!(impl->fmt->flags & AVFMT_NOFILE)) {
        ret = avio_open(&impl->oc->pb, filename, AVIO_FLAG_WRITE);
        if (ret < 0) {
            fprintf(stderr, "Could not open '%s': %s\n", filename,
                    av_err2str(ret));
            throw -1;
        }
    }

    /* Write the stream header, if any. */
    ret = avformat_write_header(impl->oc, &impl->opt);
    if (ret < 0) {
        fprintf(stderr, "Error occurred when opening output file: %s\n",
                av_err2str(ret));
        throw -1;
    }
}
